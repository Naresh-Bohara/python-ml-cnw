# 2 x 3 =6
# 2 x 4 =8 
# upto 10

# use both for and while loop

# using for loop:
num = int(input("Please enter the number for its multiplication: "))
for i in range(1, 11):
    print(f"{num} X {i} = {num*i}")

# using while loop:
num = int(input("Please enter the number for its multiplication: "))
i = 1
while (i<=10):
    print(f"{num} X {i} = {num*i}")
    i+=1


